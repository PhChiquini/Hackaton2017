    private void salvar(){
        if(!"".equals(txtSenha.getText())){
        s.setSenhaF1(txtSenha.getText());
        senhas.updateSenhaF1(s);
        }
        if(!"".equals(txtSenha2.getText())){
        s.setSenhaF2(txtSenha2.getText());
        senhas.updateSenhaF2(s);
        }
    }
    
    @Override
    public void dispose() {
        if (senhas.conexao != null) {
            try {
                senhas.closeConexao();
            } catch (Exception e) {
            }
        }
        super.dispose();
    }
    
    private void manipularBotoes(String modo) {
        switch (modo) {
            case "Inicial":
                btnCancelar.setEnabled(false);
                btnSalvar.setEnabled(true);
                txtSenha.setText("");
                txtSenha2.setText("");
                break;
            default:
                break;
        }
    }

