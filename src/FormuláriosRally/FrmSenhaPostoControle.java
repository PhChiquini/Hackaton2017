    private String posto;
    
    public void verificaPosto(int p){
        
        switch(p){
            case 1:
                posto = "Estados Unidos";
                break;
            case 2: 
                posto = "Suíça";
                break;
            case 3:
                posto = "Austrália";
                break;
            case 4:
                posto = "Alemanha";
                break;
            case 5:
                posto = "Brasil";
                break;
        }
        
        lblTitulo.setText("<html><center>Insira a Senha do posto " + posto + " para Continuar:</center></html>");
    }
    
