    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        switch(grupoObjeto.getPostoAtual()){
            case 1:
                if(edtSenhaPosto.getText().equals(fcnSenha.find(1).getSenhaP1())){
                    new FrmQuestoesFase1(1, grupoObjeto).setVisible(true);
                    this.dispose();
                }
                break;
            case 2:
                if(edtSenhaPosto.getText().equals(fcnSenha.find(1).getSenhaP2())){
                    new FrmQuestoesFase1(5, grupoObjeto).setVisible(true);
                    this.dispose();
                }
                break;
            case 3:
                if(edtSenhaPosto.getText().equals(fcnSenha.find(1).getSenhaP3())){
                    new FrmQuestoesFase1(9, grupoObjeto).setVisible(true);
                    this.dispose();
                }
                break;
            case 4:
                if(edtSenhaPosto.getText().equals(fcnSenha.find(1).getSenhaP4())){
                    new FrmQuestoesFase1(13, grupoObjeto).setVisible(true);
                    this.dispose();
                }
                break;
            case 5:
                if(edtSenhaPosto.getText().equals(fcnSenha.find(1).getSenhaP5())){
                    new FrmQuestoesFase1(17, grupoObjeto).setVisible(true);
                    this.dispose();
                }
                break;
        }
    }//GEN-LAST:event_btnContinuarActionPerformed

