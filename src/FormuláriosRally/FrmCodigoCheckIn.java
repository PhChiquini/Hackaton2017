    private void btnCheckInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckInActionPerformed
        if (edtCheckIn.getText().length() == 8) {
            grupo = fcnGrupo.find(Integer.parseInt(edtCheckIn.getText().substring(0, 3)));
            if(grupo.getCodigoGrupo() == 0)
                JOptionPane.showMessageDialog(null, "Código de Check In não encontrado!"
                        + "\nContate o Funcionário mais próximo para maiores informações.", "Check In - Rally do Conhecimento", 2);
            else{
                preenchimentoObjeto();
                
                new FrmSenhaFase(grupoObjeto).setVisible(true);
                this.dispose();
            }
        } 
        else {
            JOptionPane.showMessageDialog(null, "Código de Check In incompleto!"
                    + "\nContate o Funcionário mais próximo para maiores informações.", "Check In - Rally do Conhecimento", 1);
        }


    }//GEN-LAST:event_btnCheckInActionPerformed

