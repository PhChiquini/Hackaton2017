    @Override
    public void dispose(){
        if(fcnAgrupamento.conexao != null)
            try {
                fcnAgrupamento.closeConexao();
            } catch (SQLException e) {
            }
        if(fcnGrupo.conexao != null)
            try {
                fcnGrupo.closeConexao();
            } catch (SQLException e) {
            }
        if(fcnGabarito.conexao != null)
            try {
                fcnGabarito.closeConexao();
            } catch (SQLException e) {
            }
        if(fcnPostoDeControle.conexao != null)
            try {
                fcnPostoDeControle.closeConexao();
            } catch (SQLException e) {
            }
        super.dispose();
    }
    
    private void preenchimentoObjeto(){
        //Preenche as informações num objeto que será usado no programa todo
        grupoObjeto = new GrupoSessao();
        
        grupoObjeto.setCodigoAgrupamento(grupo.getCodigoAgrupamento());
        grupoObjeto.setCodigoGabaritoFase1(fcnGabarito.novoVazio());
        grupoObjeto.setCodigoGabaritoFase2(fcnGabarito.novoVazio());
        grupoObjeto.setCodigoGrupo(grupo.getCodigoGrupo());
        grupoObjeto.setCodigoPostoInicial(grupo.getCodigoPostoInicial());
        grupoObjeto.setNome(grupo.getNome());
        grupoObjeto.setNomePosto(fcnPostoDeControle.find(grupo.getCodigoPostoInicial()).getNomePC());
        grupoObjeto.setPontuacao(0);
        grupoObjeto.setPostoAtual(grupo.getCodigoPostoInicial());
        if(Integer.parseInt(edtCheckIn.getText().substring(7,8)) == 1)
            grupoObjeto.setLiderSegundaFase(true);
        else
            grupoObjeto.setLiderSegundaFase(false);
        //Atualiza os gabaritos
        grupo.setCodigoGabaritoFase1(grupoObjeto.getCodigoGabaritoFase1());
        fcnGrupo.update(grupo);
        agrupamento = fcnAgrupamento.find(grupo.getCodigoAgrupamento());
        agrupamento.setCodigoGabaritoFase2(grupoObjeto.getCodigoGabaritoFase2());
        fcnAgrupamento.updateAgrup(agrupamento);
    }

